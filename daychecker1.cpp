#include<stdio.h>
int main() {
	int day;
	scanf("%d", &day);
	day = day % 7;
	switch (day) {
	case 3:
		printf("월요일");
		break;

	case 4:
		printf("화요일");
		break;
	case 5:
		printf("수요일");
		break;
	case 6:
		printf("목요일");
		break;
	case 0:
		printf("금요일");
		break;
	case 1:
		printf("토요일");
		break;
	case 2:
		printf("일요일");
		break;

	}

	return 0;
}
